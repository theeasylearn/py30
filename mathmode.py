import math

print(math.ceil(4.6))
#o/p:5

print(math.copysign(3,5))
#o/p:3.0

print(math.fabs(-7.5))
#o/p:7.5

print(math.factorial(5))
#o/p:120

print(math.fmod(3,2))
#o/p:1.0

print(math.modf(5.75))
#o/p:(0.75, 5.0)

print(math.trunc(6.7))
#o/p:6
print(math.trunc(-6.7))
#o/p:-6

print(math.floor(7.8))
#o/p:7
print(math.floor(-7.8))
#o/p:-8