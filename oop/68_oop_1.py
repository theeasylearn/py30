#create class 
class Human:
    #create function
    def walk(self):
        print("I can walk...")
    def talk(self):
        print("I can talk...")
    def eat(self):
        print("I can eat...")

#create object 
ankit = Human()
ankit.walk()
ankit.talk()
ankit.eat()

#create another object
mohit = Human()
mohit.eat()
mohit.walk()
mohit.talk()